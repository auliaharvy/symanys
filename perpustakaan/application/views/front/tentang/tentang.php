            <h2 class="page-header" style="margin: 0;border-color: #ccc;">Tentang Direktori Putusan </h2>
            <br>
            

            <p>Salah satu prinsip pokok dalam sistem peradilan di dunia adalah pengadilan yang terbuka atau transparan. Keterbukaan merupakan kunci dari lahirnya akuntabilitas. Harapannya hakim dan pegawai pengadilan akan lebih profesional dalam menjalankan tugas dan tangungjawabnya.</p>

<p>Selain itu, keterbukaan informasi, yang intinya hak bagi publik untuk mengakses informasi, merupakan salah satu hak asasi manusia sebagaimana ditegaskan dalam UUD 1945. Atas pertimbangan itu, Mahkamah Agung (MA) mengeluarkan Keputusan Ketua Mahkamah Agung Nomor 144/KMA/SK/VIII/2007 tentang Keterbukaan Informasi di Pengadilan.</p>

<p>Pengembangan 'Direktori Putusan' elektronik ini adalah salah satu pelaksananaan Keputusan Ketua MA tersebut, dengan tujuan memastikan adanya informasi yang lengkap bagi masyarakat secara cepat dan murah.</p>

<p>Keputusan KMA tersebut antara lain mengatur jenis informasi yang harus diumumkan oleh pengadilan secara proaktif serta mekanisme pengumumannya. Informasi yang dimaksud adalah yang dianggap penting untuk diketahui oleh para pencari keadilan dan masyarakat, termasuk putusan dan penetapan pengadilan.</p>

<p>Putusan atau penetapan yang belum berkekuatan hukum tetap namun menarik perhatian publik, misalnya perkara korupsi, narkoba, atau terorisme, juga dapat diterbitkan oleh pengadilan.</p>


